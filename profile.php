            <p>Seiring waktu, JQH al-Mizan terus berkembang dan bertransformasi. Pada tahun 2002-2003, divisi kaligrafi didirikan untuk mewadahi minat mahasiswa dalam seni tulis Al-Qur'an. Divisi tafhim pun diubah namanya menjadi divisi tafsir pada tahun 2001-2002, mencerminkan perubahan orientasi pembelajaran menuju metodologi penjelasan makna Al-Qur'an yang sistematis.

