            <p>Jauh sebelum menjadi UKM resmi, embrio JQH al-Mizan telah ada dalam bentuk dua komunitas: BOM-F di Fakultas Syariah yang fokus pada tilawah dan halaqoh tahfizh di Fakultas Ushuludin. Antusiasme mahasiswa yang tinggi dan saran dari para pembina mendorong mereka untuk mengusulkan al-Mizan menjadi UKM tingkat universitas.

                Pada tanggal 28 Oktober 1998, UKM JQH al-Mizan resmi disahkan di bawah kepemimpinan Ujang Shihabudin. Dengan visi menyeimbangkan perjalanan mahasiswa antara budaya keislaman dan kultur akademik modern, JQH al-Mizan mulai menapaki jalannya.</p>

