            <p>JQH al-Mizan bukan hanya organisasi, tetapi juga keluarga bagi para anggotanya. Di sini, mereka tidak hanya belajar dan mengembangkan bakat seni qurani, tetapi juga menjalin persahabatan dan memperkuat keimanan.

